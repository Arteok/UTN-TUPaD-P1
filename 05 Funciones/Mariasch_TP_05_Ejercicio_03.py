#Crear una lista vacía, agregar tres palabras con append e imprimir la lista resultante por pantalla. Pista: para crear una lista vacía debes colocar los corchetes sin nada en su interior. Por ejemplo: lista_vacia = []

# Creamos una lista vacía
lista_vacia = []

# Agregamos tres palabras usando append
lista_vacia.append("Python")
lista_vacia.append("C#")
lista_vacia.append("JavaScript")

# Imprimimos la lista resultante
print("La lista resultante es:", lista_vacia)
