#Crear una lista con cinco elementos (colocar los elementos que más te gusten) y mostrar el penúltimo. ¡Puedes hacerlo como se muestra en los videos o bien investigar cómo funciona el indexing con números negativos!

# Creamos una lista con cinco elementos que me gustan
cosas_que_me_gustan = ["basket", "café", "videojuegos", "música", "programar"]

# Accedemos al penúltimo elemento usando indexación negativa 
penultimo = cosas_que_me_gustan[-2]

# Mostramos el penúltimo elemento por pantalla
print("El penúltimo elemento es:", penultimo)
